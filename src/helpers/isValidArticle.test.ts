const invalidHeaderCases = [
  ['カテゴリ', '荒らし記事', '末端カテゴリ'],
  ['カテゴリ', '荒らし記事'],
  ['カテゴリ', '不要記事'],
  ['カテゴリ', '隔離記事', '自演記事'],
] as const;

describe('isValidArticle', () => {
  it('keeps default normal article as valid', () => {
    const article = createArticle();
    const actualValid = isValidArticle(article);
    if (!actualValid) {
      throw new Error('default article should be valid');
    }
  });

  it('rejects filtered categories from headers', () => {
    for (const headers of invalidHeaderCases) {
      const article = createArticle({ header: JSON.stringify(headers) });
      const actualValid = isValidArticle(article);
      if (actualValid) {
        throw new Error(
          `[headers] expected invalid, got valid | headers: ${JSON.stringify(headers)}`,
        );
      }
    }
  });

  it('rejects article when header is malformed JSON', () => {
    const article = createArticle({ header: '[' });
    const actualValid = isValidArticle(article);
    if (actualValid) {
      throw new Error('expected invalid for malformed header JSON');
    }
  });

  it('rejects article when header JSON is not an array', () => {
    const article = createArticle({ header: JSON.stringify({ foo: 'bar' }) });
    const actualValid = isValidArticle(article);
    if (actualValid) {
      throw new Error('expected invalid for non-array header JSON');
    }
  });

  it('keeps category article valid when last header equals tag name', () => {
    const article = createArticle({
      tag_name: '荒らし記事',
      header: JSON.stringify(['隔離記事（pixiv）', '荒らし記事']),
      summary:
        'ピクシブ百科事典に存在する記事のうち荒らし扱いされるものを解説する。',
    });
    const actualValid = isValidArticle(article);
    if (!actualValid) {
      throw new Error(
        'expected valid when filtered keyword appears only as terminal self header',
      );
    }
  });

  it('rejects known invalid article from db: 忍たま小説100sers入り', () => {
    const article = createArticle({
      tag_name: '忍たま小説100sers入り',
      summary: 'タグ名間違いのため削除します。',
      header: JSON.stringify(['ピクシブ百科事典', '忍たま小説100sers入り']),
      reading: 'たぐめいまちがいのためさくじょ',
    });
    const actualValid = isValidArticle(article);
    if (actualValid) {
      throw new Error('expected invalid for known db record');
    }
  });

  it('keeps known valid article from db: 立て逃げ', () => {
    const article = createArticle({
      tag_name: '立て逃げ',
      summary: '立て逃げとは、電子掲示板などにおける迷惑行為の一種。',
      header: JSON.stringify([
        'ピクシブ百科事典',
        '一般',
        '社会',
        'インフラ',
        'ネットワーク',
        'コンピュータネットワーク',
        'インターネット',
        'ネットマナー',
        '迷惑行為',
        '立て逃げ',
      ]),
      reading: 'たてにげ',
    });
    const actualValid = isValidArticle(article);
    if (!actualValid) {
      throw new Error('expected valid for known db record');
    }
  });

  it('keeps valid summaries', () => {
    assertSummaryGroupValidity(validSummaries, true, 'valid summary');
  });

  it('rejects invalid summaries', () => {
    const uniqueInvalidSummaries = [...new Set(invalidSummaries)];
    assertSummaryGroupValidity(
      uniqueInvalidSummaries,
      false,
      'invalid summary',
    );
  });
});
