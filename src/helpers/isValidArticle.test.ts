function createArticle(overrides: Partial<PixivArticle> = {}): PixivArticle {
  return {
    tag_name: 'test-tag',
    summary: '正常な記事の概要',
    updated_at: '2026-01-01 00:00:00',
    main_illst_url: 'https://example.com/image.png',
    view_count: 1,
    illust_count: 1,
    check_count: 1,
    related_tags: '[]',
    parent: null,
    lastScraped: '2026-01-01 00:00:00',
    reading: null,
    header: '[]',
    mainText: null,
    lastScrapedReading: null,
    lastScrapedArticle: null,
    ...overrides,
  };
}

function assertSummaryValidity(
  summary: string,
  expectedValid: boolean,
  group: string
): void {
  const article = createArticle({ summary });
  const actualValid = isValidArticle(article);

  if (actualValid !== expectedValid) {
    const expectedState = expectedValid ? 'valid' : 'invalid';
    const actualState = actualValid ? 'valid' : 'invalid';
    throw new Error(
      `[${group}] expected ${expectedState}, got ${actualState} | summary: ${summary}`
    );
  }
}

